TWI = I2C(scl = 'PB9', sda = 'PB8', freq = 20000)

uart = UART(2, 9600, bits = 8, parity = None, stop = 1, timeout = 1000)

oled = OLED(TWI)
ntc = MODBUS_NTC(uart)


def map_value(v, x_min, x_max, y_min, y_max):
    return int(y_min + (((y_max - y_min)/(x_max - x_min)) * (v - x_min)))


oled.fill(oled.BLACK)
oled.show()


while(True):
    t = ntc.get_temp()
    
    if((t >= -30) and (t <= 110)):
        t_str = "T/deg C: " + str("%3.1f" %t)
    else:
        t_str = "T/deg C: ---"
        
    oled.fill(oled.BLACK)
    oled.text("PYB MODBUS NTC", 8, 4, oled.WHITE)
    oled.text(t_str, 6, 18, oled.WHITE)
    oled.blit(background, 0, 30)
    l = map_value(t, 0, 100, 0, 100)
    oled.fill_rect(20, 38, l, 4, oled.WHITE)
    oled.show()
    print(t_str)
    sleep_ms(400)
    
